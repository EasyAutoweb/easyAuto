package EasyAutoProjeto;

/**
 *
 * 
 */
public class TipoFrete {
    
    private int idTipoFrete;
    private String coditoTipoFrete;
    private String nomeTipoFrete;
    private String descricaoTipoFrete;

    public TipoFrete() {
    }

    public int getIdTipoFrete() {
        return idTipoFrete;
    }

    public void setIdTipoFrete(int idTipoFrete) {
        this.idTipoFrete = idTipoFrete;
    }

    public String getCoditoTipoFrete() {
        return coditoTipoFrete;
    }

    public void setCoditoTipoFrete(String coditoTipoFrete) {
        this.coditoTipoFrete = coditoTipoFrete;
    }

    public String getNomeTipoFrete() {
        return nomeTipoFrete;
    }

    public void setNomeTipoFrete(String nomeTipoFrete) {
        this.nomeTipoFrete = nomeTipoFrete;
    }

    public String getDescricaoTipoFrete() {
        return descricaoTipoFrete;
    }

    public void setDescricaoTipoFrete(String descricaoTipoFrete) {
        this.descricaoTipoFrete = descricaoTipoFrete;
    }
    
    
    
}
